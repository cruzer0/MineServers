    <h1>Regeln</h1>
</div>
<h4>§1 Registrierung</h4>
<ol>
    <li>Jede natürliche Person darf genau einen Account besitzen, diesen muss sie selber registriert haben.</li>
    <li>Die Weitergabe von Accountdaten ist strengstens untersagt, sollte ein Account abhanden kommen so ist jeder Benutzer selbst dafür verantwortlich.</li>
</ol>
<h4>§2 Server</h4>
<ol>
    <li>Für jeden Server darf nur ein Eintrag vorhanden sein, bei mehrfachem Verstoß kann es zur Sperrung der IP in unserem System kommen.</li>
    <li>Nur der Betreiber des Servers oder ein von ihm ernannter Vertreter darf einen Server eintragen.</li>
    <li>Es dürfen keine Inhalte gepostet werden für die der Nutzer keine ausreichenden Rechte besitzt. Außerdem sind Links zu anderen Serverlisten strengstens verboten.</li>
    <li>Es ist untersagt mutmaßlich falsche Angaben in die Serverbeschreibung einzubauen.</li>
</ol>
<h4>§3 Votes</h4>
<ol>
    <li>Jeder Nutzer darf nur einmal pro Tag voten. Die Aufzeichnungen (IP, Benutzername) werden jeden Tag um 0:00 aus unserem System entfernt.</li>
    <li>Die Nutzung von Bots, Scripts, Proxys oder sonstigen Programmen zur Steigerung der Vote-Zahl ist untersagt.</li>
    <li>Du erklärst dich als Nutzer von mineservers.eu einverstanden, dass bei Übermittlung eines Votes, deine IP-Adresse und dein Nutzername gespeichert und an den Server gesendet werden, für den du votest. Diese Daten werden Täglich um 0:00 Uhr aus unserem System entfernt.</li>
</ol>
<h4>§4 Kommentare</h4>
<ol>
    <li>Angemeldete Benutzer können Server kommentieren. Dieser Kommentar darf nicht beleidigend sein und keine Unwahrheiten enthalten.</li>
    <li>Kommentare können von unseren Moderatoren jederzeit gelöscht werden.</li>
    <li>Werbung ist in Kommentaren untersagt, Missachtung dieser Regel kann zu einer dauerhaften Sperre führen.</li>
    <li>Kommentare müssen sinnvoll sein, Kommentare wie "GEIL" oder "Joint" sind untersagt.</li>
</ol>
<h4>§5 Allgemeine Bestimmungen</h4>
<ol>
    <li>Der Betreiber kann die Regeln jederzeit, ohne vorherige Ankündigung und mit sofortiger Wirkung ändern. Der Benutzer ist selber dafür verantwortlich sich auf aktuellem Stand zu halten!</li>
    <li>Jeder Nutzer ist für seine Einträge selber verantwortlich. Wir übernehmen keine Haftung für Benutzereinträge.</li>
</ol>
